package com.mycompany.neview.model.elements;

/**
 *
 * @author mugarov
 */
public class Coordinates {
    private final double x;
    private final double y;
    
    public Coordinates(double x, double y){
        this.x = x;
        this.y = y;
    }
    
    public double getX(){
        return this.x;
    }
    
    public double getY(){
        return this.y;
    }
}
